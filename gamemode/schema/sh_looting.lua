if (SERVER) then
	SCHEMA.RespawnTimer = SCHEMA.RespawnTimer or CurTime()

	hook.Add("Think", "LookThink", function()
		if (CurTime() > SCHEMA.RespawnTimer) then
			SCHEMA.RespawnTimer = SCHEMA.RespawnTimer + 1200
			SCHEMA:CleanupCrates()
			local CratesToSpawn = SCHEMA:GetCrateCount() - SCHEMA:GetCrates()
			for i = 1, CratesToSpawn do
				SCHEMA:SpawnStash(table.Random(SCHEMA.Lootpos[game.GetMap()]))
			end
		end
	end)

	function SCHEMA:GetCrates()
		return #ents.FindByClass("forp_stash") or 0
	end

	-- how many crates to spawn?
	function SCHEMA:GetCrateCount()
		if (#player.GetAll() < 10) then
			return 25
		elseif (#player.GetAll() < 20) then
			return 50
		elseif (#player.GetAll() < 30) then
			return 75
		elseif (#player.GetAll() < 45) then
			return 100
		elseif (#player.GetAll() < 60) then
			return 125
		elseif (#player.GetAll() < 70) then
			return 150
		elseif (#player.GetAll() < 90) then
			return 200
		end
	end

	function SCHEMA:CleanupCrates()
		for k, v in pairs(ents.FindByClass("forp_stash")) do
			local found = false
			for k2, v2 in pairs(ents.FindInSphere(v:GetPos(), 1024)) do
				if (v2:IsPlayer()) then
					found = true
				end
			end
			if (!found) then
				v:Remove()
			end
		end
	end

	function SCHEMA:GetCratesOnMap()
		return #ents.FindByClass("forp_stash")
	end

	function SCHEMA:SpawnStash(pos)
		for k, v in pairs(ents.FindInSphere(pos, 256)) do
			if (v.stash) then
				return
			end
		end
		for k, v in pairs(ents.FindInSphere(pos, 2048)) do
			if (v:IsPlayer()) then
				return
			end
		end

		local stash = ents.Create("forp_stash")
		stash:SetPos(pos + Vector(0,0,15))
		stash.stash = true
		stash:Spawn()
	end
end